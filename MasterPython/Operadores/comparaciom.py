if year != 2021:
    print('No estamos a 2021')
else:
    print('Si estamos a 2021')

if year > 2021:
    print('Estamos en 2021 o un año mas adelante')
else:
    print('Estamos a 2021')

if year < 2021:
    print('Estamos a un año antes!')
else:
    print('Estamos a un año despues que el 2021')

if year >= 2021:
    print('Estamos en 2021 o un año mas adelante')
else:
    print('No estamos a 2021')

if year <= 2021:
    print('Estamos a un año antes que el 2021')
else:
    print('No estamos a 2021')

